import SwiftUI

struct GlobeSpinner: View {
    @State private var rotation = 0.0

    var body: some View {
        Image(systemName: "globe")
            .foregroundStyle(.blue)
            .rotationEffect(.degrees(rotation))
            .onAppear {
                withAnimation(.linear(duration: 1).repeatForever(autoreverses: false)) {
                    rotation = 360
                }
            }
    }
}
